import { describe, it, expect } from 'vitest';
import { numberToWords } from './format';

describe('numberToWords — рубли и копейки', () => {
  it('ноль', () => {
    expect(numberToWords(0)).toBe('Ноль рублей 00 копеек');
  });

  it('1 — мужской род для рублей', () => {
    expect(numberToWords(1)).toBe('Один рубль 00 копеек');
  });

  it('2 — рубля', () => {
    expect(numberToWords(2)).toBe('Два рубля 00 копеек');
  });

  it('5 — рублей', () => {
    expect(numberToWords(5)).toBe('Пять рублей 00 копеек');
  });

  it('11 — рублей (исключение 11-14)', () => {
    expect(numberToWords(11)).toBe('Одиннадцать рублей 00 копеек');
  });

  it('21 — рубль (последняя цифра 1, но не 11)', () => {
    expect(numberToWords(21)).toBe('Двадцать один рубль 00 копеек');
  });

  it('22 — рубля', () => {
    expect(numberToWords(22)).toBe('Двадцать два рубля 00 копеек');
  });
});

describe('numberToWords — тысячи (женский род)', () => {
  it('1000 — одна тысяча', () => {
    expect(numberToWords(1000)).toBe('Одна тысяча рублей 00 копеек');
  });

  it('2000 — две тысячи', () => {
    expect(numberToWords(2000)).toBe('Две тысячи рублей 00 копеек');
  });

  it('5000 — пять тысяч', () => {
    expect(numberToWords(5000)).toBe('Пять тысяч рублей 00 копеек');
  });

  it('11000 — одиннадцать тысяч (исключение)', () => {
    expect(numberToWords(11000)).toBe('Одиннадцать тысяч рублей 00 копеек');
  });

  // Это была главная регрессия v1.3: «двадцать один тысяч»
  it('21000 — двадцать одна тысяча', () => {
    expect(numberToWords(21000)).toBe('Двадцать одна тысяча рублей 00 копеек');
  });

  it('22000 — двадцать две тысячи', () => {
    expect(numberToWords(22000)).toBe('Двадцать две тысячи рублей 00 копеек');
  });

  it('25000 — двадцать пять тысяч', () => {
    expect(numberToWords(25000)).toBe('Двадцать пять тысяч рублей 00 копеек');
  });

  it('1500 — одна тысяча пятьсот', () => {
    expect(numberToWords(1500)).toBe('Одна тысяча пятьсот рублей 00 копеек');
  });
});

describe('numberToWords — копейки', () => {
  it('0.01 — одна копейка', () => {
    expect(numberToWords(0.01)).toBe('Ноль рублей 01 копейка');
  });

  it('0.05 — пять копеек', () => {
    expect(numberToWords(0.05)).toBe('Ноль рублей 05 копеек');
  });

  it('1.50 — один рубль 50 копеек', () => {
    expect(numberToWords(1.5)).toBe('Один рубль 50 копеек');
  });

  it('350.00 — типичный кейс из накладной', () => {
    expect(numberToWords(350)).toBe('Триста пятьдесят рублей 00 копеек');
  });
});

describe('numberToWords — миллионы (мужской род)', () => {
  it('1 000 000 — один миллион', () => {
    expect(numberToWords(1_000_000)).toBe('Один миллион рублей 00 копеек');
  });

  it('2 000 000 — два миллиона', () => {
    expect(numberToWords(2_000_000)).toBe('Два миллиона рублей 00 копеек');
  });

  it('5 000 000 — пять миллионов', () => {
    expect(numberToWords(5_000_000)).toBe('Пять миллионов рублей 00 копеек');
  });

  it('1 234 567 — комбинированная сумма', () => {
    expect(numberToWords(1_234_567)).toBe(
      'Один миллион двести тридцать четыре тысячи пятьсот шестьдесят семь рублей 00 копеек'
    );
  });
});
